package p_preview;

public class NestedLoop {

	public static void main(String[] args) {
		int dan;
		int times;
		
		for(dan=2; dan<=19; dan++) {
			for(times=1; times<=9; times++) {
				System.out.println(dan + " X " + times + " = " + (dan*times));
			}
			System.out.println( );
		}
		System.out.println("榴芭款 备备窜 寇快扁 场~!! ^___^ b");
	}

}
